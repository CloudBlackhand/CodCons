// Função para buscar conteúdo por palavra-chave
function searchCDCContent(query) {
  const results = [];
  const searchTerm = query.toLowerCase();
  
  // Buscar em todos os capítulos e artigos
  Object.keys(CDC_CONTENT).forEach(chapterKey => {
    const chapter = CDC_CONTENT[chapterKey];
    
    if (chapter.title && chapter.title.toLowerCase().includes(searchTerm)) {
      results.push({
        type: 'chapter',
        key: chapterKey,
        title: chapter.title,
        content: chapter.title
      });
    }
    
    if (chapter.articles) {
      Object.keys(chapter.articles).forEach(articleKey => {
        const article = chapter.articles[articleKey];
        
        if (article.title && article.title.toLowerCase().includes(searchTerm)) {
          results.push({
            type: 'article',
            chapterKey,
            key: articleKey,
            title: article.title,
            number: article.number,
            content: article.content
          });
        }
        
        if (article.content && article.content.toLowerCase().includes(searchTerm)) {
          results.push({
            type: 'content',
            chapterKey,
            key: articleKey,
            title: article.title,
            number: article.number,
            content: article.content
          });
        }
      });
    }
  });
  
  return results;
}

// Função para obter artigo específico
function getArticle(chapterKey, articleKey) {
  if (CDC_CONTENT[chapterKey] && CDC_CONTENT[chapterKey].articles && CDC_CONTENT[chapterKey].articles[articleKey]) {
    return CDC_CONTENT[chapterKey].articles[articleKey];
  }
  return null;
}

// Função para obter capítulo específico
function getChapter(chapterKey) {
  return CDC_CONTENT[chapterKey] || null;
}

// Função para obter lista de capítulos
function getChaptersList() {
  return Object.keys(CDC_CONTENT).map(key => ({
    key,
    title: CDC_CONTENT[key].title
  }));
}

